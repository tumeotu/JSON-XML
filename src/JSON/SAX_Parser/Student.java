package JSON.SAX_Parser;

public class Student {

    public String ID;
    public String Name;
    public String Code;
    public int Age;

    @Override
    public String toString() {
        return "Student{" +
                "ID='" + ID + '\'' +
                ", Name='" + Name + '\'' +
                ", Code='" + Code + '\'' +
                ", Age='" + Age + '\'' +
                '}';
    }
}
